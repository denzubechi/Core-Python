#The range() function returns a sequence of numbers, starting from 0 by default,
# and increments by 1 (by default), and ends at a specified number.

for x in range(6):
  print(x)
for y in range(2, 6):
  print(y)
  
for z in range(2, 30, 3):
  print(z)